package basic.classes;

public class MathOperations {
	
	public static int addNums(int num1, int num2) {
		return num1 + num2;
	}
	
	public static int subtractBfromA(int numA, int numB) {
		return numA - numB;
	}
	
	public static int multiplyNums(int num1, int num2) {
		return num1 * num2;
	}
	
	public static double divideAbyB(double numA, double numB) {
		return numA / numB;
	}
	
}
