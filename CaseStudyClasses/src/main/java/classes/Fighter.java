package classes;

public class Fighter {
	
	private int id;
	private String name;
	private double sigLpm;
	private double sigApm;
	private double tdpF;
	private double subPF;
	private double koPF;	
	private double finRate;
	
	public Fighter(String name, double sigLpm, double sigApm, double tdpF, double subPF, double koPF, double finRate) {
		this.name = name;
		this.sigLpm = sigLpm;
		this.sigApm = sigApm;
		this.tdpF = tdpF;
		this.subPF = subPF;
		this.koPF = koPF;
		this.finRate = finRate;
	}
	
	public Fighter(int id, String name, double sigLpm, double sigApm, double tdpF, double subPF, double koPF, double finRate) {
		this.name = name;
		this.id = id;
		this.sigLpm = sigLpm;
		this.sigApm = sigApm;
		this.tdpF = tdpF;
		this.subPF = subPF;
		this.koPF = koPF;
		this.finRate = finRate;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getSigLpm() {
		return sigLpm;
	}
	public void setSigLpm(double sigLpm) {
		this.sigLpm = sigLpm;
	}
	public double getSigApm() {
		return sigApm;
	}
	public void setSigApm(double sigApm) {
		this.sigApm = sigApm;
	}
	public double getSubpF() {
		return subPF;
	}
	public void setSubpF(double subpF) {
		this.subPF = subpF;
	}
	public double getTdpF() {
		return tdpF;
	}
	public void setTdpF(double tdpF) {
		this.tdpF = tdpF;
	}
	public double getFinRate() {
		return finRate;
	}
	public void setFinRate(double finRate) {
		this.finRate = finRate;
	}

	public double getKoPF() {
		return koPF;
	}

	public void setKoPF(double koPF) {
		this.koPF = koPF;
	}
		
}
